FPGA芯片:为现场可编程门阵列(Field Programmable Gate Array);
是逻辑芯片的一种，逻辑芯片还包括 CPU、GPU、DSP 等通用处理器芯片，以及专用集成电路芯片 ASIC
集成电路芯片=数字+模拟;数字=存储+逻辑
FPGA，主要由“可编程逻辑单元+输入输出单元+开关连线阵列”等三种功能单元构成。
FPGA 和 CPU、GPU、ASIC 的等核心区别：其底层逻辑运算单元的连线及逻辑布局未固化，适用于底层算法需要持续更迭的运算领域，例如人工智能算法优化。
而其他类别逻辑芯片，像 ASIC、CPU 和 GPU 等，物理底层逻辑单元的运算关系均已固定且不可变。
并且 FPGA 不需要像 CPU 和 GPU 在软件应用层面的指令系统编译，对 FPGA 进行编程是使用硬件描述语言，并直接编译烧录为晶体管电路的组合，即直接用晶体管电路实现用户的算法
由于 FPGA 是一种半定制芯片，具有现场可编程性，特别适用于需求物理运算逻辑需要持续更迭的应用领域，例如人工智能算法优化、数据中心应用等。

全球 FPGA 芯片需求规模超过 125 亿美金，年复合增速超过 10%
中国 FPGA 市场高需求，国产化趋势下助力国内企业占领市场
高灵活性使得下游应用领域丰富，是 FPGA 芯片需求增长核心源动力。
5G对FPGA芯片的大需求+通道数增加，计算复杂度增加，FPGA 单价有望进一步提高
人工智能时代 DEEP LEARNING 是重要趋势，FPGA 具有独特优势:在云侧处理时，FPGA 芯片的高灵活性及强并行运算能力突出；在端侧处理时，可编程性、高吞吐和低延迟等特点能有效满足需求。
汽车自动化->FPGA低延迟+高吞吐:自动驾驶等级提高，所需传感器数量增长，需处理的传感器数据将成指数级提升
