第三代半导体
什么是第三代半导体:相对于第一代（硅基）半导体，第三代半导体（碳化硅等）禁带宽度大，电导率高、热导率高。
在高压、高功率、高频的分立器件领域，硅因其窄带隙，较低热导率和较低击穿电压限制了其在该领域的应用，因而发展出宽禁带、耐高压、高热导率、高频的第二/三代半导体。
耐高压、高功率能力。第三代半导体材料能量密度更高。

碳化硅更适合作为衬底材料;碳化硅衬底器件体积小,电阻小,能量损失小;
碳化硅MOSFET电动车续航里程更长
应用:碳化硅基-碳化硅外延功率器件、碳化硅基-氮化镓外延射频器件:AC->AC(变压器)、AC->DC(整流器)、DC->AC(逆变器)、DC->DC(升降压变换器)
产业规模持续增加(44e->61e),港真,规模真心不大;复合增长速度极快;

