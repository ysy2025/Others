1,镍矿背景:
	1.1 全球镍矿的主要分布在红土镍矿(占 55%),硫化矿(占比 28%),以及海底锰结核中(占比 17%)。
		红土镍矿主要分布在赤道附近的巴西新喀里多尼亚、古巴、印尼、菲律宾、哥伦比亚等国。
		硫化矿则主要分布在澳大利亚、俄罗斯、加拿大、中国和南非。
		镍矿供应集中,政策干扰较大。

	1.2 硫化矿和红土矿的二元冶炼路径
		硫化矿->红土矿;
