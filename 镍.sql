印尼禁止镍出口:
2014年1月12日，原矿出口禁令在印尼正式生效，即日起，在印尼采矿的企业必须在当地冶炼或精炼后方可出口。
也就是说,当地有镍冶炼产能才好.
根据2017年的矿业开采法规，印尼计划于2022年1月12日开始暂停未加工矿石出口，此前已给予矿商五年的时间来在国内建造冶炼厂。随后外国矿企入驻印尼的脚步加快。

